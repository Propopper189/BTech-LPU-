class break1
{
    public static void main(String args[])
    {
        for(int i = 0; i <= 5; i++)
        {
            System.out.println(i);
            System.out.println(i + 1);
            System.out.println(i + 2);
            break;
            System.out.println(i + 3);
            System.out.println(i + 4);
        }
    }
}