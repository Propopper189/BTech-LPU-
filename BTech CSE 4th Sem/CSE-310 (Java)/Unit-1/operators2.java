class operators2
{
    public static void main(String args[])
    {
        // LogicalOperators : (&&, ||)
        System.out.println(true && false);
        System.out.println(true || false);
        
        // MCQ :
        int a = 5, b = 6, c = 7, d = 8;
        System.out.println(a < b && c > d);
    }
}