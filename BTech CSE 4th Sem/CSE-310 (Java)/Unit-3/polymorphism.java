// PolyMorPhism -> poly = many, morph = forms

// process of creating many objects of different classes place them in groups to call data menmbers and member functions to carry out different operations.

// 2 types of polymorphism : 
    // 1. Compile time polymorphism (overloading) 
        // 1 class 2 or more functions with same names and different arguments
        // return type and arguments of function checked at compile time 

    // 2. Run time polymorphism (overriding)
        // return type and arguments of function checked at run time 
        // possible in inheritance
        // if there are 2 classes, one parent, another child, 
        // parent and child both have the same function with same arguments